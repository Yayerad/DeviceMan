    public boolean verify() throws SQLException {
        boolean valid=false;
        Admin admins[] = dbman.getadmin();
        int i = 0 ;
        while(admins[i] != null && !valid){
        if(admins[i].getUsername().equals(txtUsername.getText().trim()) && admins[i].getPassword().equals(txtPassword.getText()) && admins[i].isDeactivate()==false ){
            valid = true;
            user = admins[i];
     
        }
        i++;

        }
        return valid;
    }
