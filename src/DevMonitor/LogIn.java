    private void btnLogInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogInActionPerformed
          if (txtUsername.getText().trim().equals("") || txtPassword.getPassword().length == 0)
        {
            //if field is empty
            JOptionPane.showMessageDialog(null, "Please enter username and password.");
            
        }
        else //both fields are filled
        {
            try 
            {
                if (verify()) //calling verify method to validate credintials
                {  
                    Home home = new Home ();
                     home.setVisible(true);
                    this.setVisible(false);
                }
                else //invalid details were entered
                {
                    JOptionPane.showMessageDialog(null, "Invalid login details entered or your account may have been suspended by the administrator");
                }
            } catch (SQLException ex)
            {
                Logger.getLogger(LogIn.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    
      
          
    }//GEN-LAST:event_btnLogInActionPerformed

